def parse_uploaded_file(file) -> List[dict]:
    """Accept CSV or JSON file. Required columns: reviewer_name, rating,
    platform_source, text, review_date."""
    name = file.name.lower()
    if name.endswith(".json"):
        data = json.load(io.TextIOWrapper(file, encoding="utf-8"))
        if isinstance(data, dict):
            data = data.get("reviews", [])
        return data
    if name.endswith(".csv"):
        df = pd.read_csv(file)
        return df.to_dict(orient="records")
    raise ValueError("Only .csv and .json files are supported.")


def normalise_review(raw: dict) -> dict:
    """Pick the fields the backend wants and coerce types. Be lenient on column names."""
    def get(*keys, default=None):
        for k in keys:
            if k in raw and raw[k] not in (None, ""):
                return raw[k]
        return default

    rating = get("rating", "stars", "score")
    try:
        rating = int(rating)
    except (TypeError, ValueError):
        rating = 3
    rating = max(1, min(5, rating))

    return {
        "reviewer_name": str(get("reviewer_name", "author_name", "name", default="Anonymous")),
        "rating": rating,
        "platform_source": str(get("platform_source", "platform", "source", default="unknown")),
        "review_date": str(get("review_date", "date", default=datetime.utcnow().strftime("%Y-%m-%d"))),
        "text": str(get("text", "review_text", "content", default="")),}


