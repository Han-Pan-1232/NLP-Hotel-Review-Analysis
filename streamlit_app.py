def _headers() -> Dict[str, str]:
    h = {"Content-Type": "application/json"}
    if st.session_state.user_id:
        h["X-User-Id"] = st.session_state.user_id
    return h

def api_call(method: str, path: str, *, json_body: Optional[dict] = None,
             params: Optional[dict] = None, auth_required: bool = True) -> Any:
    """Single-point HTTP wrapper. Returns parsed JSON or raises on error."""
    url = f"{API_BASE_URL}{path}"
    if auth_required and not st.session_state.user_id:
        raise RuntimeError("Not logged in — register a user first on the Home page.")
    try:
        resp = requests.request(
            method, url,
            headers=_headers(),
            json=json_body,
            params=params,
            timeout=REQUEST_TIMEOUT,)
    except requests.exceptions.ConnectionError:
        raise RuntimeError(f"Cannot reach backend at {API_BASE_URL}.")
    except requests.exceptions.Timeout:
        raise RuntimeError(f"Backend timed out after {REQUEST_TIMEOUT}s.")

    if resp.status_code >= 400:
        try:
            detail = resp.json().get("detail", resp.text)
        except Exception:
            detail = resp.text
        raise RuntimeError(f"{resp.status_code}: {detail}")
    if resp.status_code == 204 or not resp.content:
        return None
    return resp.json()

